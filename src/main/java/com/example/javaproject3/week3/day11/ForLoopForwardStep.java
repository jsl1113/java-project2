package com.example.javaproject3.week3.day11;

public class ForLoopForwardStep {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i += 2) {
            System.out.printf("%d ", i);
        }
        System.out.println();
        for (int i = 1; i < 10; i += 2) {
            System.out.printf("%d ", i);
        }
    }
}
